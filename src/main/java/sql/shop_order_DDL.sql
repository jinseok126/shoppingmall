
  CREATE TABLE SHOP_ORDER 
   (	PRODUCT_ID VARCHAR2(20 BYTE), 
	PURCHASE_COUNT NUMBER, 
	PURCHASE_PRICE NUMBER, 
	PAYMENT_DATE VARCHAR2(20 BYTE), 
	DELIVERY VARCHAR2(1 BYTE), 
	ID VARCHAR2(20 BYTE),
	order_num NUMBER
   );


CREATE SEQUENCE shop_order_seq
START WITH 1
